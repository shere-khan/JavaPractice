package java.cert.practice;

public class PrimitiveDataTypes {
	public static void main(String[] args) {
		byte b = 0; // 8-bit
		short s = 0; // 16-bi
		int i = 0; // 32-bit
		long l = 0L; // 64-bit
		float f = 0.0f; // single precision 32-bit floating point
		double d = 0.0; // double precision 64-bit floating point
		boolean bool = false;
		char c = 0; // single 16-bit Unicode character 0 <= char <= 65,535
		char aChar = 'H'; // single quotes for character
		String str = "Hello World!";
	}
}
